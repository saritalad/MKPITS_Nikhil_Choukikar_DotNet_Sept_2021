#include<stdio.h>

int main()

{

   int x = 21, y = 55;
   printf("%d\n", x);
   printf("%i\n", y);
   float f = 78.56;
   printf("%f\n", f); //print float value
   printf("%e\n", f); //print in scientific notation
   int a = 88;
   printf("%o\n", a); //print in octal format
   printf("%x\n", a); //print in hexadecimal format
   char str[] = "Hello World";
   printf("%s\n", str);
   
}

